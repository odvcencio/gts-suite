package generated

// Signature defines how to recognize files from a specific code generator.
type Signature struct {
	Generator string
	Language  string
	Patterns  []string
	Markers   []string
}

var builtinRegistry []Signature

func init() {
	builtinRegistry = defaultRegistry()
}

func defaultRegistry() []Signature {
	return []Signature{
		// Go
		{Generator: "protobuf", Language: "go", Patterns: []string{"*.pb.go", "*_grpc.pb.go"}, Markers: []string{`Code generated by protoc-gen-go`}},
		{Generator: "sqlc", Language: "go", Markers: []string{`Code generated by sqlc`}},
		{Generator: "go-generate", Language: "go", Markers: []string{`(?m)^// Code generated .* DO NOT EDIT\.$`}},
		// Python
		{Generator: "protobuf", Language: "python", Patterns: []string{"*_pb2.py", "*_pb2_grpc.py", "*_pb2.pyi"}},
		// JS/TS
		{Generator: "protobuf", Language: "javascript", Patterns: []string{"*_pb.js", "*_pb.ts", "*_pb.d.ts"}},
		// C/C++
		{Generator: "protobuf", Language: "c", Patterns: []string{"*.pb.h", "*.pb.cc", "*.pb.c"}},
		// Swift
		{Generator: "protobuf", Language: "swift", Patterns: []string{"*.pb.swift"}},
		// Ruby
		{Generator: "protobuf", Language: "ruby", Patterns: []string{"*_pb.rb"}},
		// Universal
		{Generator: "unknown", Language: "*", Markers: []string{`@generated`, `AUTO-GENERATED`, `AUTOGENERATED`}},
	}
}
