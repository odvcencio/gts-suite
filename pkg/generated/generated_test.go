func TestDetect_FilenamePattern(t *testing.T) {
	d := NewDetector(nil)
	info := d.Detect("user.pb.go", nil)
	if info == nil {
		t.Fatal("expected detection for *.pb.go")
	}
	if info.Generator != "protobuf" {
		t.Errorf("expected protobuf, got %s", info.Generator)
	}
	if info.Reason != "filename" {
		t.Errorf("expected filename, got %s", info.Reason)
	}
}

