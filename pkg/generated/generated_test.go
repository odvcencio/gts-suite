func TestMatchGlob(t *testing.T) {
	cases := []struct {
		pattern string
		path    string
		want    bool
	}{
		{"api/**/*.pb.go", "api/v1/user.pb.go", true},
		{"api/**/*.pb.go", "api/v1/v2/deep/user.pb.go", true},
		{"api/**/*.pb.go", "other/user.pb.go", false},
		{"internal/gen/**", "internal/gen/foo.go", true},
		{"internal/gen/**", "internal/gen/sub/bar.go", true},
		{"internal/gen/**", "internal/other/foo.go", false},
		{"*.pb.go", "user.pb.go", true},
		{"*.pb.go", "sub/user.pb.go", false},
	}
	for _, tc := range cases {
		got := matchGlob(tc.pattern, tc.path)
		if got != tc.want {
			t.Errorf("matchGlob(%q, %q) = %v, want %v", tc.pattern, tc.path, got, tc.want)
		}
	}
}
