func TestDetect_AllLanguagePatterns(t *testing.T) {
	d := NewDetector(nil)
	cases := []struct {
		path      string
		wantGen   string
		wantFound bool
	}{
		// Go
		{"user.pb.go", "protobuf", true},
		{"user.pb.gw.go", "protobuf-gateway", true},
		{"mock_repo.go", "mockgen", true},
		{"repo_mock.go", "mockgen", true},
		{"wire_gen.go", "wire", true},
		{"size_string.go", "", false}, // stringer now detected only by marker, not filename
		{"color_enumer.go", "enumer", true},
		{"zz_generated.deepcopy.go", "deepcopy", true},
		// Python
		{"user_pb2.py", "protobuf", true},
		{"user_pb2_grpc.py", "protobuf", true},
		// JS/TS
		{"user_pb.ts", "protobuf", true},
		{"user_pb.js", "protobuf", true},
		{"types.generated.ts", "graphql-codegen", true},
		{"types.generated.tsx", "graphql-codegen", true},
		// Java
		{"UserServiceGrpc.java", "grpc", true},
		{"DaggerAppComponent.java", "dagger", true},
		{"User_Factory.java", "dagger", true},
		// C/C++
		{"user.pb.h", "protobuf", true},
		{"user.pb.cc", "protobuf", true},
		{"lex.yy.c", "flex", true},
		{"parser.tab.c", "bison", true},
		{"parser.tab.h", "bison", true},
		{"moc_widget.cpp", "moc", true},
		// C#
		{"Form1.designer.cs", "auto-generated", true},
		{"Form1.Designer.cs", "auto-generated", true},
		{"Widget.g.cs", "auto-generated", true},
		// Swift
		{"user.pb.swift", "protobuf", true},
		{"User+CoreDataProperties.swift", "coredata", true},
		// Ruby
		{"user_pb.rb", "protobuf", true},
		// Not generated
		{"main.go", "", false},
		{"handler.py", "", false},
		{"index.ts", "", false},
	}
	for _, tc := range cases {
		info := d.Detect(tc.path, nil)
		if tc.wantFound {
			if info == nil {
				t.Errorf("Detect(%q): expected %s, got nil", tc.path, tc.wantGen)
				continue
			}
			if info.Generator != tc.wantGen {
				t.Errorf("Detect(%q): expected generator %q, got %q", tc.path, tc.wantGen, info.Generator)
			}
		} else {
			if info != nil {
				t.Errorf("Detect(%q): expected nil, got %+v", tc.path, info)
			}
		}
	}
}

func TestDetect_MarkerSignatures(t *testing.T) {
	d := NewDetector(nil)
	cases := []struct {
		name    string
		path    string
		header  string
		wantGen string
	}{
		{"sqlc", "query.go", "// Code generated by sqlc. DO NOT EDIT.", "sqlc"},
		{"ent", "client.go", "// Code generated by ent, DO NOT EDIT.", "ent"},
		{"gorm", "model.go", "// Code generated by gorm.io/gen. DO NOT EDIT.", "gorm-gen"},
		{"go-bindata", "bindata.go", "// Code generated by go-bindata. DO NOT EDIT.", "go-bindata"},
		{"bindgen", "ffi.rs", "/* automatically generated by rust-bindgen */", "bindgen"},
		{"lombok", "User.java", "// @lombok.Generated", "lombok"},
		{"thrift", "types.go", "// Autogenerated by Thrift Compiler", "thrift"},
		{"django", "0001_initial.py", "# Generated by Django 4.2", "django-migrations"},
		{"pydantic", "model.py", "# generated by datamodel-code-generator", "pydantic-codegen"},
		{"sourcery", "Mock.swift", "// Generated using Sourcery 2.0", "sourcery"},
		{"cmake", "FooConfig.cmake", "# Generated by CMake", "cmake"},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			info := d.Detect(tc.path, []byte(tc.header+"\npackage x\n"))
			if info == nil {
				t.Fatalf("expected detection for %s", tc.name)
			}
			if info.Generator != tc.wantGen {
				t.Errorf("expected %s, got %s", tc.wantGen, info.Generator)
			}
		})
	}
}

