func TestDetect_OnlyScansFirst20Lines(t *testing.T) {
	d := NewDetector(nil)
	lines := ""
	for i := 0; i < 20; i++ {
		lines += "// normal comment\n"
	}
	lines += "// Code generated by sqlc. DO NOT EDIT.\n"
	info := d.Detect("querier.go", []byte(lines))
	if info != nil {
		t.Fatalf("should not detect marker past line 20, got %+v", info)
	}
}

func TestDetect_ProtobufAcrossLanguages(t *testing.T) {
	d := NewDetector(nil)
	cases := []struct {
		path string
		want bool
	}{
		{"user.pb.go", true},
		{"user_pb2.py", true},
		{"user_pb.ts", true},
		{"user.pb.h", true},
		{"user.pb.cc", true},
		{"user.pb.swift", true},
		{"user_pb.rb", true},
		{"user.go", false},
	}
	for _, tc := range cases {
		info := d.Detect(tc.path, nil)
		got := info != nil
		if got != tc.want {
			t.Errorf("Detect(%q): got detected=%v, want %v", tc.path, got, tc.want)
		}
	}
}

